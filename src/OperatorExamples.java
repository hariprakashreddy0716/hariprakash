public class OperatorExamples {
    public static void main(String[] args){
        for(int i=0;i<=10;i++){
            System.out.println("i=="+i);
        }
        int i=0;
        while(i<=10){
            System.out.println("i=="+i);
            i++;
        }
        int i1 = 12;
        do{
            System.out.println("i1 ="+i1);
            i++;
        }while(i1<=10);
    }
}
